const getCallData = async (contract_address: string, token_id: string, value: string) => {
    // encode function data
	const contract = getProvider(contract_address)
	const callData = contract.interface.encodeFunctionData("listNFT", [contract_address, token_id, ethers.parseEther(value)])
    return callData
}

const getProvider = (
	contract_address: string
) => {
    const provider = new ethers.JsonRpcProvider("https://rpc.ankr.com/eth_sepolia")
	return new ethers.Contract("0xe2b8392a9d449ec0d2d377dc2a3c9cceb36e80d4", NFT_MARKETPLACE_ABI, provider);
}
 
export const POST = frames(async (ctx) => {

    // get url params
    const { contract_address, token_id } = ctx.searchParams;
    console.log("contract_address", contract_address)
    console.log("token_id", token_id)
    const value = "1"
    const calldata= await getCallData(contract_address, token_id, value)
 
  // Return transaction data that conforms to the correct type
  return transaction({
    chainId: "eip155:11155111", // OP Mainnet
    method: "eth_sendTransaction",
    params: {
      abi: NFT_MARKETPLACE_ABI,
      to: "0xe2b8392a9d449ec0d2d377dc2a3c9cceb36e80d4",
      data: calldata,
      value: "0",
    },
  });
});